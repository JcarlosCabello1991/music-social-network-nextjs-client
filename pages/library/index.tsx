import { useDispatch, useSelector } from 'react-redux';
import { RootState } from '../../redux/store';
import Head from 'next/head';
import Layout from '../../components/Layout/Layout';
import Row from '../../components/Row/Row';
import { useGetAlbumsQuery } from '../../redux/albumAPI';
import GridLayout from '../../components/GridLayout/GridLayout';
import RowSkeleton from '../../components/RowSkeleton/RowSkeleton';
import Button from '@mui/material/Button';
import { useI18N } from '../../context/i18';
import { useEffect, useState } from 'react';
import styles from './styles.module.css';
import { useGetArtistsQuery } from '../../redux/artistAPI';

type Props = {};
interface View {
  [view: string]: boolean;
}

const Library = (props: Props) => {
  const [background, setBackground] = useState(false);

  const [filter, setFilter] = useState<View>({
    all: true,
  });
  const store = useSelector((state: RootState) => state);
  const { data: albums, isLoading, error } = useGetAlbumsQuery(undefined);
  const {
    data: artists,
    isLoading: isLoadingArtist,
    error: artistsError,
  } = useGetArtistsQuery(undefined);
  const { t } = useI18N();

  const handleView = (view: string) => {
    setFilter({ [view]: true });
  };

  return (
    <>
      <Head>
        <title>{`${t('additional').app_name} - ${t('home').library}`}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <div className={`${styles.library_header} ${styles.library_header_bg}`}>
          <Button
            variant="outlined"
            color="inherit"
            onClick={() => handleView('all')}
            className={filter.all ? styles.active_view : styles.inactive_view}
          >
            {t('additional').all}
          </Button>
          <Button
            variant="outlined"
            color="inherit"
            onClick={() => handleView('albums')}
            className={
              filter?.albums ? styles.active_view : styles.inactive_view
            }
          >
            {t('additional').albums}
          </Button>
          <Button
            variant="outlined"
            color="inherit"
            onClick={() => handleView('artists')}
            className={
              filter?.artists ? styles.active_view : styles.inactive_view
            }
          >
            {t('additional').artists}
          </Button>
          <Button
            variant="outlined"
            color="inherit"
            onClick={() => handleView('playlists')}
            className={
              filter?.playlists ? styles.active_view : styles.inactive_view
            }
          >
            {t('additional').playlist}
          </Button>
        </div>
        <div className={styles.library_content}>
          {filter?.all ? (
            <>
              {isLoading ? (
                <RowSkeleton />
              ) : (
                <Row title={t('additional').albums} data={albums?.data} />
              )}
              {isLoading ? (
                <RowSkeleton />
              ) : (
                <Row title={t('additional').artists} data={albums?.data} />
              )}
              {isLoading ? (
                <RowSkeleton />
              ) : (
                <Row title={t('additional').playlist} data={albums?.data} />
              )}
            </>
          ) : (
            <></>
          )}
          {filter?.albums ? <GridLayout data={albums?.data} /> : <></>}
          {filter?.artists ? <GridLayout data={artists?.data} /> : <></>}
          {filter?.playlists ? <GridLayout data={artists?.data} /> : <></>}
        </div>
      </Layout>
    </>
  );
};

export default Library;
