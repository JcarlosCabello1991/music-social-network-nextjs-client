import Layout from "../../components/Layout/Layout";

import styles from "./styles.module.css";
import Head from "next/head";
import TabPanel from "../../components/TabPanel/TabPanel";
import { useRouter } from "next/router";
import { useGetPlaylistDetailsQuery } from "../../redux/playlistAPI";
import CreatePlaylist from "../../components/Playlist/CreatePlaylist";
import Searchbar from "../../components/Playlist/Searchbar/Searchbar";
import TrackList from "../../components/TrackList/TrackList";
import { useI18N } from "../../context/i18";
import DeleteButton from "../../components/Playlist/DeleteButton/DeleteButton";
import { useGetTracksQuery } from "../../redux/trackAPI";
import { useEffect, useState } from "react";

const Playlist = (tracks: any) => {
  tracks = tracks.tracks;

  const { query } = useRouter();
  const { t } = useI18N();

  const {
    data: playlist,
    isLoading: isLoadingPlaylist,
    error: playlistError,
  } = useGetPlaylistDetailsQuery(query.playlistID, {
    refetchOnMountOrArgChange: true,
  });

  // const {
  //   data: tracks,
  //   isLoading: isLoadingTracks,
  //   error: tracksError,
  // } = useGetTracksQuery(undefined);
  // const tracksData = tracks?.data;
  const [results, setResults] = useState([]);
  const [search, setSearch] = useState("");

  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    if (e.target.value.length > 0) {
      setSearch(e.target.value);
      setResults(
        tracks.filter((track: any) =>
          track.title
            .toLowerCase()
            .replace(/\s/g, "")
            .includes(search.toLowerCase().replace(/\s/g, ""))
        )
      );
    } else {
      setSearch("");
      setResults([]);
    }
  };

  useEffect(() => {
    if (search.length > 0) {
      const results = tracks.filter((track: any) =>
        track.title
          .toLowerCase()
          .replace(/\s/g, "")
          .includes(search.toLowerCase().replace(/\s/g, ""))
      );
      setResults(results);
    } else {
      setSearch("");
      setResults([]);
    }
  }, [search]);

  const tracksExistInPlaylist = playlist?.data?.tracks?.length > 0;
  return (
    <>
      <Head>
        <title>{`${t("additional").app_name} - ${
          t("additional").playlist
        }`}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Layout>
        <CreatePlaylist
          playlistId={true}
          title={playlist?.data?.title}
          image={playlist?.data?.image}
          description={playlist?.data?.description}
        />
        {tracksExistInPlaylist ? (
          <>
            <div className={styles.playlist_tracks}>
              <TrackList
                name={playlist?.data?.title}
                tracks={playlist?.data?.tracks}
              />
            </div>
            <Searchbar handleChange={handleChange} />
            {results.length && search !== " " ? (
              <div className={styles.search_results}>
                <TrackList
                  name={`${results.length} results for "${search}"`}
                  tracks={results}
                />
              </div>
            ) : search !== "" ? (
              <div className={styles.no_results}>
                <h3>No results found for &quot;{search}&quot;</h3>
              </div>
            ) : null}
          </>
        ) : (
          <>
            <Searchbar handleChange={handleChange} />
            {results.length && search !== " " ? (
              <div className={styles.search_results}>
                <TrackList
                  name={`${results.length} results for "${search}"`}
                  tracks={results}
                />
              </div>
            ) : search !== "" ? (
              <div className={styles.no_results}>
                <h3>No results found for &quot;{search}&quot;</h3>
              </div>
            ) : null}
          </>
        )}
      </Layout>
    </>
  );
};

export async function getServerSideProps() {
  const req = await fetch("http://localhost:4002/track");
  const data = await req.json();
  const res = data.data;

  return {
    props: {
      tracks: res,
    },
  };
}

export default Playlist;
